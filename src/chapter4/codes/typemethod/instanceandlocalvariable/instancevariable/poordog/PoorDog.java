package chapter4.codes.typemethod.instanceandlocalvariable.instancevariable.poordog;

public class PoorDog {
  private int age;
  private double size ;
  private String name;
  private boolean isBig;
  private char gradeDog;

  public int getAge() {
    return age;
  }

  public double getSize(){
    return size;
  }
  public String getName(){
    return name;
  }
  public boolean getIsBig(){
    return isBig;
  }
  public char getGradeDog(){
    return gradeDog;
  }
}
